

x = 0
y = 0
def init(a, b):
    global x
    global y
    x = a
    y = b

def subtraction(): 
    return x - y

def multipliction(): 
    return x * y

def summ():
    return x + y

def division():
    return x / y

def integer_division():
    return x // y
    
def remainder_of_division():
    return x % y



